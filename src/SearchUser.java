    private void searchByNameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchByNameButtonActionPerformed
        //Method that search by name, if it's empty, will show all the user
        removeAllDataFromLabel();
        showResultList.removeAll();
        String nametoSearch;
        nametoSearch=nameInserted.getText();
        list =personList.getListByName(nametoSearch);
        for(int i=0;i<list.size();i++)
            showResultList.add(list.get(i));
        
        
    }//GEN-LAST:event_searchByNameButtonActionPerformed

    private void showResultListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_showResultListMouseClicked
        if(showResultList.getItem(0).equals("Person not found")){
            
        }else{
            String nameChoosen=showResultList.getSelectedItem().substring(0,8);
            Person p = personList.getPersonFromUniqueId(nameChoosen);
            nameLabel.setText(p.getName());
            surnameLabel.setText(p.getSurname());
            idLabel.setText(p.getUniqueId());
            if(p.isStudent())
                roleLabel.setText("STUDENT");
            else
                if(p.isTeacher()){
                    roleLabel.setText("TEACHER");
                }else{
                    roleLabel.setText("OFFICE W.");
                }
            if (personList.getPersonFromIndex(personList.getPersonLogged()).isStudent()){
                mobileLabel.setText("PRIVACY");
                dobLabel.setText("PRIVACY");
                addressLabel.setText("PRIVACY");
            }else{
                mobileLabel.setText(p.getMobile());
                dobLabel.setText(p.getDob());
                addressLabel.setText(p.getAddress());
            }
            courseLabel.setText(courseList.getNameCourseFromCode(p.getCourseId()));
        }
        
    }//GEN-LAST:event_showResultListMouseClicked

    private void searchByIdButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchByIdButtonActionPerformed
        //Method that search by the ID, if it's empty, will show an error
        showResultList.removeAll();
        removeAllDataFromLabel();
        String idToSearch= idinserted.getText();
        Person p = personList.getPersonFromUniqueId(idToSearch);
        if(p==null)
            showResultList.add("Person not found");
        else{
            nameLabel.setText(p.getName());
            surnameLabel.setText(p.getSurname());
            dobLabel.setText(p.getDob());
            addressLabel.setText(p.getAddress());
            idLabel.setText(p.getUniqueId());
            mobileLabel.setText(p.getMobile());
            courseLabel.setText(courseList.getNameCourseFromCode(p.getCourseId()));
        }
    }//GEN-LAST:event_searchByIdButtonActionPerformed

