    private void sortinButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortinButtonActionPerformed
        /*Method who sort all the result, the button change in order of what the user want
        he can use ascendin order or descending order by name*/
        showResultList.removeAll();
        String[]sorted;
        if(list.isEmpty()){
           
       }else{
            sorted=new String[list.size()];
            for(int i=0;i<list.size();i++)
                sorted[i]=list.get(i);
            sorted=sorting(sorted);
        if(sortinButton.getText().equals("A-Z")){
           sortinButton.setText("Z-A");
           for(int i=0;i<sorted.length;i++)
               showResultList.add(sorted[i]);
        }else{
           sortinButton.setText("A-Z");
           //to get the second order, the system just back print the list of the first order
           for(int i=sorted.length-1;i>=0;i--)
               showResultList.add(sorted[i]);
        }
       }
    }//GEN-LAST:event_sortinButtonActionPerformed
    private String[] sorting(String[] unsorted){
         for(int j=0; j<unsorted.length;j++)
 
        for (int i=j+1 ; i<unsorted.length; i++)
        {
         if(unsorted[i].substring(9,unsorted[i].length()).compareTo(unsorted[j].substring(9, unsorted[j].length()))<0)
         {
            
             String temp= unsorted[j];
             unsorted[j]= unsorted[i]; 
             unsorted[i]=temp;


         }
     }
         return unsorted;

    }
